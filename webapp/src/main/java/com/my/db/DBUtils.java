package com.my.db;

import java.sql.*;

public class DBUtils {

    public static IDAO dao = DAOFactory.getDAOInstance(TypeDAO.MySQL);

    public static void close(Statement statement) {
        if (statement != null) {
            try {
                statement.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
    public static void close(ResultSet resultSet) {
        if (resultSet != null) {
            try {
                resultSet.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
}
